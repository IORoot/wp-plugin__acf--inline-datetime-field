if( ! defined( 'ABSPATH' ) ) exit;


// check if class already exists
if( !class_exists('andyp_acf_plugin_inline_datetime_picker') ) :

class andyp_acf_plugin_inline_datetime_picker {
	
	// vars
	var $settings;
	
	
	/*
	*  __construct
	*
	*  This function will setup the class functionality
	*
	*  @type	function
	*  @date	17/02/2016
	*  @since	1.0.0
	*
	*  @param	void
	*  @return	void
	*/
	
	function __construct() {
		
		// settings
		// - these will be passed into the field class.
		$this->settings = array(
			'version'	=> '1.0.0',
			'url'		=> plugin_dir_url( __FILE__ ),
			'path'		=> plugin_dir_path( __FILE__ )
		);
		
		
		// include field
		add_action('acf/include_field_types', 	array($this, 'include_field')); // v5
	}
	
	
	/*
	*  include_field
	*
	*  This function will include the field type class
	*
	*  @type	function
	*  @date	17/02/2016
	*  @since	1.0.0
	*
	*  @param	$version (int) major ACF version. Defaults to false
	*  @return	void
	*/
	
	function include_field( $version = false ) {
				
		// include
		include_once('fields/class-andyp-acf-field-inline_date_time_picker.php');
		return;
	}
	
}


// initialize
new andyp_acf_plugin_inline_datetime_picker();


// class_exists check
endif;
	
